                <h2 class="heading-separator">Formation certifiante</h2>
                <p class="subheading-text">A côté des diplômes bien connus de tous, il existe des formations très opérationnelles et généralement plus courtes qui débouchent sur un certificat ou un titre professionnel.
                Mais qu’est ce qu’une formation certifiante ?
                Souvent moins chères que les formations diplômantes, les formations certifiantes sont appréciées des recruteurs et très valorisées sur un CV car leur enseignement est en lien direct avec les besoins des entreprises d’une branche donnée. C’est là leur force, mais aussi leur faiblesse car de ce fait elles restent dédiées à un métier précis. La formation certifiante n’est donc pas adapté à quelqu’un qui cherche des compétences transversales lui permettant d’évoluer dans plusieurs secteurs d’activité.
                L’Académie CISCO Locale de l’IAI-TOGO offre trois curricula :<br/>
                IT Essentials,
                CCNA ( Il se décompose en quatre (4) modules: CCNA1, CCNA2, CCNA3 et CCNA4),
                CCNA Security.


                </p>
                
            

